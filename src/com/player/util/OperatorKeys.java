	public static int getKey(String operator, String key){
		JSONObject data;
		int value = 0;
		String Operator="";
		if(Constant.DEBUG)  Log.d("OperatorKeys" , "Operator "+ operator + " key " + key);
		try {
			InputStream is = context.getAssets().open("codes.json");
	        int size = is.available();
	        byte[] buffer = new byte[size];
	        is.read(buffer);
	        is.close();
	        String json = new String(buffer, "UTF-8");
			data = new JSONObject(json);
			if(data != null){
	        	JSONArray jsonArray = data.getJSONArray("data");
	        	if(jsonArray != null && jsonArray.length() > 0) {
	        		for(int i=0; i<jsonArray.length(); i++) {
						JSONObject jsonObject = jsonArray.getJSONObject(i);
						Operator = jsonObject.getString("operator");
						if (Operator.equalsIgnoreCase(operator)){
							JSONArray jsonArrayForKey = jsonObject.getJSONArray("keys");
							if(jsonArrayForKey != null && jsonArrayForKey.length() > 0) {
								for(int j=0; j<jsonArrayForKey.length(); j++) {
									JSONObject Key = jsonArrayForKey.getJSONObject(j);
									if(Key != null) {
										value = Key.getInt(key);
										if(Constant.DEBUG)  Log.d("OperatorKeys" , "value "+value);
									}
								}
							}
						}
	        		}
	        		
	        	}
	        }
//			if (Operator.equalsIgnoreCase("tatasky")){
//				JSONArray keyArray = data.getJSONArray("keys");
//				for (int i = 0; i < keyArray.length(); i++) {
//					JSONObject obj = keyArray.getJSONObject(i);
//					value = obj.getInt(key);
//					if(Constant.DEBUG)  Log.d("OperatorKeys" , "value "+value);
//				}
//			}
		} catch (Exception e) {
			e.printStackTrace();
			StringWriter errors = new StringWriter();
			e.printStackTrace(new PrintWriter(errors));
			SystemLog.createErrorLogXml(SystemLog.TYPE_PLAYER,SystemLog.LOG_IR, errors.toString(), e.getMessage());
		}
		return value;
	}
	
